# Conversor de temperatura

# ºC para °F

temperatura_Celsius = float(input("Digite a temperatura em °C: \n"))

temperatura_Fahrenheit = ((9 * temperatura_Celsius) / 5) + 32

print("A temperatura em Celsius é: %.2f\n" % temperatura_Celsius)
print("A temperatura em Fahrenheit é: %.2f\n" % temperatura_Fahrenheit)
